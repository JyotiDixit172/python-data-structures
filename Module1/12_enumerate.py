# enumerate is a built in python function -> which works on any iterable 
# iterables like lists, tuples

#  it has nothing to do with hashmaps or dictionaries. -> enumerate is just a looping helper.

# enumerate() -> in simple language, just adds a counter while you loop over something
nums = [1,2,34,55]
for i,n in enumerate(nums):
    print(i,n)

